<h3>Principes de base</h3>
<h4>
<ul>
         <li>   Chaque enfant est unique, et les activit&#233;s  &#233;ducatives qui lui sont propos&#233;es doivent respecter son rythme de d&#233;veloppement, ses besoins et ses champs d'int&#233;r&ecirc;t.</li>
		<li> L'enfant est le premier agent de son d&#233;veloppement, c'est-&agrave;-dire que ce d&#233;veloppement part d'une aptitude et d'une motivation intrins&egrave;ques &agrave; l'enfant. L'adulte guide ensuite cette d&#233;marche et la soutient afin qu'elle conduise l'enfant &agrave; l'autonomie.</li>
       <li>Le d&#233;veloppement de l'enfant est un processus global et int&#233;gr&#233; qui comporte plusieurs dimensions : affective, physique et motrice, sociale et morale, cognitive, et langagi&egrave;re.</li>
