<p>Il devient enfin de plus en plus autonome, d&#233;veloppe sa capacit&#233; de se nourrir, de se v &ecirc;tir ou de se d&#233;tendre seul, et acquiert de saines habitudes de vie, de saines habitudes alimentaires ainsi que des comportements qui influencent de mani&egrave;re positive sa sant&#233; et son bien- &ecirc;tre.</p>

 
</h4>
  <h3>Jeu</h3>

  <h4><p> Un programme &#233;ducatif destin&#233 aux jeunes enfants met in&#233vitablement l'accent sur le jeu. Celui-ci constitue pour l'enfant l'instrument par excellence pour explorer son univers, le comprendre, le ma&icirc;triser. Il faut donc le consid&#233;rer comme l'outil essentiel d'expression, de compr&#233;hension et d'int&#233;gration de l'enfant.<BR>

Le jeu permet &agrave;l'enfant de se d&#233velopper et de se r&#233aliser sur tous les plans. Il peut prendre plusieurs formes. Qu'il s'agisse de jeux solitaires, parall&egrave;les, associatifs ou coop&#233ratifs, de jeux d'exercice, symboliques, d'assemblage ou de construction, avec des r&egrave;gles simples ou complexes, ils devraient  &ecirc;tre favoris&#233;s puisqu'ils conduisent &agrave;des apprentissages diff&#233;rents. Par les jeux de construction, l'enfant d&#233;couvre les lois de l'&#233;quilibre. Par les jeux de r&egrave;gles, il acquiert des habilet&#233;s sociales essentielles &agrave; la vie en groupe.</P><BR>

 
</h4>
<h3>Programme adapt&#233; &agrave; chaque milieu</h3>

<h4><p>La garderie applique les principes du programme &#233;ducatif en tenant compte de son contexte, de ses ressources et des possibilit&#233;s que lui offre son environnement. Il adapte le programme &agrave; l'	&acirc;ge des enfants et &agrave; la p&#233;riode de la journ&#233;e pendant laquelle ils fr&#233;quentent la garderie.<BR>

Si les activit&#233;s peuvent varier, le programme &#233;ducatif quotidien doit &#233;tres  quilibr&#233; : jeux &agrave; l'int&#233;rieur et &agrave; l'ext&#233;rieur, jeux calmes et actifs, activit&#233;s individuelles et collectives, en petit et en grand groupe, en veillant toujours &agrave; la sant&#233;, &agrave; la s&#233;curit&#233; et au bien- &ecirc;tre des enfants.</p><BR><BR>

 
</h4>
<h3>Personnel &#233;ducateur</h3>
<h4>
<p>Gr&acirc;ce &agrave; ses comp&#233;tences et&agrave;son expertise, le personnel &#233;ducateur guide les jeux des enfants et recueille, en les observant, des renseignements essentiels pour les accompagner dans leurs activit&#233;s. Il met &agrave;leur disposition ce dont ils ont besoin pour se livrer&agrave; des jeux de toutes sortes et convenant aux go&ucirc;ts et aux possibilit&#233;s de chacun. Il soutient les enfants et les encourage tout au long de la journ&#233;e, dans les jeux auxquels ceux-ci s'adonnent depuis toujours comme dans les nouveaux jeux qu'ils imaginent, et guide indirectement les acquis qu'un enfant tire d'une situation dans laquelle il s'est lui-m &ecirc;me engag&#233;.</p><BR>

 

