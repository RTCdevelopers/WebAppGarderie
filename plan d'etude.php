<h3>Dimensions du d&#233;veloppement de l'enfant</h3>  <BR><h4>

        &agrave; la garderie, l'enfant a la possibilit&#233; d'acqu&#233;rir ou de ma&icirc;triser plusieurs attitudes ou habilet&#233;s :</p>
		<ul>

       <li> Dimension affective : L'enfant apprend &agrave; &#233;tablir des liens de confiance avec des adultes autres que ses parents,&agrave;exprimer et &agrave; contr&ocirc;ler ses &#233;motions, &agrave; vivre des changements et des transitions, &agrave; avoir confiance en lui et &agrave; d&#233;velopper son identit&#233; personnelle et sexuelle. </li>

      <li> Dimension physique et motrice : L'enfant d&#233;veloppe sa perception sensorielle, sa motricit&#233; globale et fine, sa coordination, sa lat&#233;ralisation ainsi que son sch&#233;ma corporel.</li>
         <li> Dimension sociale et morale : L'enfant apprend &agrave; s'entendre avec ses pairs, &agrave; s'int&#233;grer &agrave;un groupe, &agrave; respecter les diff&#233;rences &agrave; coop&#233;rer, &agrave; exercer son leadership et &agrave; prendre en compte la perspective des autres avant d'agir.</li>

      <li>  Dimension cognitive : L'enfant apprend &agrave; s'organiser dans l'espace et dans le temps, &agrave; structurer sa pens&#233;e,&agrave; raisonner,&agrave; d&#233;duire, &agrave; comprendre le monde qui l'entoure et &agrave; r&#233;soudre des probl&egrave;mes.</li>
